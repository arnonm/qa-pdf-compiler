def language_code_to_full_name(code: str) -> str:
    """Return English name for ISO 639-1 (2-letter) or common ISO 639-2 (3-letter) code."""
    c = (code or "").strip().lower()
    if not c:
        return ""
    if len(c) == 2 and c in ISO_639_1:
        return ISO_639_1[c]
    if len(c) == 3:
        alpha2 = _ISO_639_2_TO_1.get(c)
        if alpha2 and alpha2 in ISO_639_1:
            return ISO_639_1[alpha2]
    if len(c) == 2:
        return c.upper()
    if len(c) == 3:
        return c.upper()
    return code.strip()
